/**
 * My math library
 * @module MyMath
 */

/**
 * A function that multiplies two values
 * @param {Number} a first value
 * @param {Number} b second value
 * @returns {Number} total
 */
const multiply = (a, b) => {
  return a * b;
};

/**
 * A function that divides two values
 * @param {Number} a first value
 * @param {Number} b second value
 * @returns {Number} total
 */
const divide = (a, b) => {
  return a / b;
};

export { multiply, divide };
